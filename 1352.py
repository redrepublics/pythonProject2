x = int(input('Вводим первое число по координатам'))
y = int(input('Вводим второе число по координатам'))

if x > 0 and y > 0:
    print("Первая четверть")
if x > 0 and y < 0:
    print("Четвертая четверть")
if x < 0 and y > 0:
    print("Вторая четверть")
if x < 0 and y < 0:
    print("Третья четверть")

